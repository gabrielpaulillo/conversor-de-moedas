package br.com.alura;

public class ConversorDeMoeda {
//    Função que recebe como parâmetros 2 floats, um referente à 'quantia' e outro ao valor da taxa
//    Essa retorna um float da quantia multiplicada pela taxa
    public float converterParaMoeda(float quantia, float taxa) {
        return quantia * taxa;
    }
}
